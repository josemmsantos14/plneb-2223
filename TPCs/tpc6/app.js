function showEng() {
  var x = document.getElementById("eng-desc");
  var y = document.getElementById("eng-pic");
  var z = document.getElementById("eng-btn");
  if (x.style.display === "none") {
    x.style.display = "block";
    y.style.marginTop = "1rem";
    z.text = "Less";
  } else {
    x.style.display = "none";
    y.style.marginTop = "8rem";
    z.text = "More";
  }
}
